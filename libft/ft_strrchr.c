#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i])
		i++;
	while (i >= 0)
	{
		if (s[i] == (char)c)
			return ((char *)s + i);
		i--;
	}
	return (0);
}
/*Cette fonction recherche la première occurrence du caractère 
passé en second paramètre dans la chaîne de caractères spécifiée 
via le premier paramètre.*/